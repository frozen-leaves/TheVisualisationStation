package uk.ac.manchester.cs.m84556jh.colour;

public class Col2 {
	private int hue;
	private int sat;
	
	public Col2(int givenHue, int givenSat) {
		this.hue = givenHue;
		this.sat = givenSat;
	}

	public int getHue() {
		return hue;
	}

	public int getSat() {
		return sat;
	}
	
}
